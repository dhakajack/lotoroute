export function getItemsForMode(mode: GameMode): PlateItem[] {
  if (mode === "france") {
    return FRENCH_DEPARTMENTS;
  }

  if (mode === "countries") {
    return COUNTRY_CODES;
  }

  return ALL_PLATE_ITEMS;
}

export function getPlateItemByCode(code: string): PlateItem | null {
  return ALL_PLATE_ITEMS.find((item) => item.code === code) ?? null;
}

